#include <sys/common.h>
#include <sys/debug.h>
#include <sys/log.h>

#define OUTPUT_BASE	0xFF000000	/* physical output device address */

static uint32_t *output = (uint32_t*)OUTPUT_BASE;

void Log::writeChar(char c) {
	/* some chars make no sense here */
	if(c != '\r' && c != '\b')
		*output = c;
}
